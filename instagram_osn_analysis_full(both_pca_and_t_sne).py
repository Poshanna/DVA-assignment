"""## 4. Text Preprocessing
Extract captions, remove URLs, mentions, and calculate lengths before and after for stage-wise visualization.
"""

import re

def preprocess_text(text):
    text = re.sub(r'http\S+', '', text) # Remove URLs
    text = re.sub(r'@\w+', '', text) # Remove Mentions
    text = re.sub(r'#\w+', '', text) # Remove Hashtags optionally
    text = re.sub(r'\s+', ' ', text).strip() # Extra spaces
    return text.lower()

original_lengths = []
clean_lengths = []
processed_texts = []
languages_list = []

for record in loaded_data:
    raw = record['content_data']['text_caption']
    clean = preprocess_text(raw)

    original_lengths.append(len(raw))
    clean_lengths.append(len(clean))
    processed_texts.append(clean)
    languages_list.append(record['content_data']['language'])

print("--- Preprocessed First 5 Records ---")
for i in range(5):
    print(f"[{languages_list[i]}] Original: {loaded_data[i]['content_data']['text_caption']}")
    print(f"[{languages_list[i]}] Cleaned : {processed_texts[i]}\n")

