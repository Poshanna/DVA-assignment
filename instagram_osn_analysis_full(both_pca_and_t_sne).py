"""## 5. Vectorization Techniques (IndicBERT)
Extracting contextual embeddings without needing a Hugging Face token, natively applying `google/muril-base-cased`.
"""

from transformers import AutoTokenizer, AutoModel
import torch
from sklearn.decomposition import PCA
from sklearn.manifold import TSNE

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"Using device: {device}")

# No token required for public models
model_identifier = "google/muril-base-cased"
tokenizer = AutoTokenizer.from_pretrained(model_identifier)
model = AutoModel.from_pretrained(model_identifier).to(device)

# Vectorize first 300 samples for reasonable time and scatter plot
sample_texts_subset = processed_texts[:300]
sample_langs_subset = languages_list[:300]

inputs = tokenizer(sample_texts_subset, padding=True, truncation=True, return_tensors="pt", max_length=64).to(device)
with torch.no_grad():
    outputs = model(**inputs)
    # CLS token
    embeddings = outputs.last_hidden_state[:, 0, :].cpu().numpy()

print("Embeddings shape extracted:", embeddings.shape)

# Stage 3 Visualization: Comparison of PCA and t-SNE Projections
pca = PCA(n_components=2)
components_pca = pca.fit_transform(embeddings)
df_pca = pd.DataFrame({'PCA 1': components_pca[:, 0], 'PCA 2': components_pca[:, 1], 'Language': sample_langs_subset})

tsne = TSNE(n_components=2, random_state=42)
components_tsne = tsne.fit_transform(embeddings)
df_tsne = pd.DataFrame({'t-SNE 1': components_tsne[:, 0], 't-SNE 2': components_tsne[:, 1], 'Language': sample_langs_subset})

fig, axes = plt.subplots(1, 2, figsize=(18, 8))
